export function getDejsomritrutaiPefr(ageStr: number | string, heightCm: number | string, isMale: boolean): number | null {
  const age = Math.round(Number(ageStr));
  let h = Math.round(Number(heightCm));
  if (isNaN(age) || isNaN(h)) return null;
  // Snap height to nearest even number between 150-184
  if (h < 150) h = 150;
  if (h > 184) h = 184;
  if (h % 2 !== 0) h -= 1;
  const hIndex = PEF_REFERENCE.heights.indexOf(h);
  if (hIndex === -1) return null;
  const genderData = isMale ? PEF_REFERENCE.M : PEF_REFERENCE.F;
  // Constrain age
  let lookupAge = age;
  if (lookupAge < 15) lookupAge = 15;
  if (lookupAge > 95) lookupAge = 95;
  const row = (genderData as any)[lookupAge];
  if (!row) return null;
  return row[hIndex];
}